package com.icbc.config;

public class ICBCSession {

	public String soaCertString;
	public String sysNo;
	public String userId;
	public String getSoaCertString() {
		return soaCertString;
	}
	public void setSoaCertString(String soaCertString) {
		this.soaCertString = soaCertString;
	}
	public String getSysNo() {
		return sysNo;
	}
	public void setSysNo(String sysNo) {
		this.sysNo = sysNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
}
